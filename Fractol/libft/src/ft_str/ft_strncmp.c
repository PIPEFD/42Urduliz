#include "../../inc/libft.h"

int ft_strncmp(char *s1, char *s2, size_t n)
{
    if (NULL == s1 || NULL == s1 || n <= 0)
        return (0);
    while (*s1 == *s2 && n > 0 && *s1 != '\0')
    {
        s1++;
        s2++;
        n--;
    }
    return (*s1 - *s2);
}
 